    <div class="container">
        <h2>Typische Probleme – und wie wir sie zuverlässig lösen</h2>
        
        <p>Viele unserer Einsätze beginnen mit kleinen Alltagsproblemen, die schnell größer werden können. Als Fachbetrieb erkennen wir die Ursache, handeln schnell und sorgen dafür, dass das Problem nicht erneut auftritt. Hier sind fünf typische Fälle aus der Praxis – und was wir konkret tun:</p>
        
        <div class="problems-grid">
            <div class="problem-item">
                <h3>✅ Steckdose defekt oder locker</h3>
                <p>Lose oder ausgebrannte Steckdosen sind nicht nur nervig, sondern können zur echten Gefahr werden. Wir prüfen die Verdrahtung, tauschen defekte Einsätze und stellen die sichere Funktion wieder her – schnell und fachgerecht.</p>
                
                <h3>✅ Lichtschalter funktioniert nicht</h3>
                <p>Ob Wackelkontakt oder völliger Ausfall: Wir tauschen defekte Lichtschalter zügig aus und prüfen die Stromzufuhr. Auf Wunsch modernisieren wir auch direkt auf smarte Schaltertechnik.</p>
                
                <h3>✅ Sicherung fällt ständig raus</h3>
                <p>Wiederholte Sicherungsausfälle sind ein Warnsignal. Wir messen die Leitungen durch, prüfen auf Kurzschlüsse oder Überlastung und beheben die Ursache nachhaltig – für einen sicheren Betrieb.</p>
                
                <h3>✅ Stromausfall in Wohnung oder Haus</h3>
                <p>Plötzlicher Stromausfall? Wir lokalisieren die Fehlerquelle – ob im Sicherungskasten, FI-Schalter oder an einzelnen Stromkreisen – und sorgen für die schnelle Wiederherstellung der Versorgung.</p>
            </div>
            
            <div class="problem-item">
                <h3>✅ Kurzschluss hinter der Wand</h3>
                <p>Ein verschmorter Geruch oder Lichtausfall kann auf einen gefährlichen Kurzschluss hindeuten. Wir lokalisieren die Stelle präzise mit modernen Messgeräten und tauschen nur, was nötig ist – sauber und mit minimalem Eingriff.</p>
                
                <h3>✅ Alte Elektrik modernisieren</h3>
                <p>Veraltete Elektroinstallationen entsprechen oft nicht mehr den aktuellen Sicherheitsstandards. Wir bringen Ihre Anlage auf den neuesten Stand – von der Verkabelung bis zum Sicherungskasten.</p>
                
                <h3>✅ FI-Schalter löst aus</h3>
                <p>Wenn der FI-Schalter häufig auslöst, liegt meist ein Fehler in der Installation vor. Wir finden die Ursache schnell und beheben sie dauerhaft.</p>
                
                <h3>✅ Elektrogeräte funktionieren nicht</h3>
                <p>Wenn Elektrogeräte plötzlich nicht mehr funktionieren, kann das an der Stromversorgung liegen. Wir prüfen die Anschlüsse und stellen die Funktion wieder her.</p>
            </div>
        </div>
    </div>
</section>
